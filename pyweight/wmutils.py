def lbs_to_kg(lbs):
    return lbs * 0.45359237


def kg_to_lbs(kg):
    return kg / 0.45359237


def in_to_cm(inch):
    return inch * 2.54


def cm_to_in(cm):
    return cm / 2.54


def in_to_m(inch):
    return inch * 0.0254


def cm_to_m(cm):
    return cm / 100


def m_to_in(m):
    return m / 0.0254


def m_to_cm(m):
    return m * 100
